package com.javatpoint.model;

public class TicketLevelInfo {

	private String ticketLevelPaxType;

	private String ticketIndicator;

	private String ticketIssueDate;

	public String getTicketIssueDate() {
		return ticketIssueDate;
	}

	public void setTicketIssueDate(String ticketIssueDate) {
		this.ticketIssueDate = ticketIssueDate;
	}

	public String getTicketLevelPaxType() {
		return ticketLevelPaxType;
	}

	public void setTicketLevelPaxType(String ticketLevelPaxType) {
		this.ticketLevelPaxType = ticketLevelPaxType;
	}

	public String getTicketIndicator() {
		return ticketIndicator;
	}

	public void setTicketIndicator(String ticketIndicator) {
		this.ticketIndicator = ticketIndicator;
	}

}
